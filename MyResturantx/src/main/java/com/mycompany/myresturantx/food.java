package com.mycompany.myresturantx;

public class food {
    private String name;    // The name of the food item
    private double price;   // The price of the food item in SAR
    private int calories;   // The number of calories in the food item

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPrice() {
        return price;
    }

    public void setPrice(double price) {
        this.price = price;
    }

    public int getCalories() {
        return calories;
    }

    public void setCalories(int calories) {
        this.calories = calories;
    }
}
